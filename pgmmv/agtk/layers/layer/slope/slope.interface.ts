/**
 * Agtk slope interface.
 */
export interface AgtkSlope {
  /**
   * Name.
   */
  name: string;

  /**
   * Start x coordinate.
   */
  startX: number;

  /**
   * Start y coordinate.
   */
  startY: number;

  /**
   * End x coordinate.
   */
  endX: number;

  /**
   * End y coordinate.
   */
  endY: number;
}
